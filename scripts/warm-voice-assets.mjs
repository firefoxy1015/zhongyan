import { readFile, writeFile } from "node:fs/promises";

const endpoint = process.env.VOICE_WARM_ENDPOINT ?? "https://zhongyan.onrender.com/api/voice";
const characters = ["tiantian", "qiao", "xiao", "zhao", "han", "zhang", "li", "lin", "qixia"];
const lines = characters.flatMap((characterId) => [
  { key: `${characterId}:testimony`, characterId, speakerId: characterId, kind: "testimony" },
  { key: `${characterId}:followUp`, characterId, speakerId: "qixia", kind: "followUp" },
]);

const destination = new URL("../app/lib/voice-assets.ts", import.meta.url);

function renderManifest(assets) {
  return `import type { CharacterVoiceId, VoiceLineKind } from "./testimony-speech.ts";\n\nexport type VoiceAssetKey = \`${"${CharacterVoiceId}:${VoiceLineKind}"}\`;\n\n// Generated by scripts/warm-voice-assets.mjs. Do not replace these URLs at runtime.\nexport const VOICE_ASSET_URLS: Readonly<Partial<Record<VoiceAssetKey, string>>> = Object.freeze(${JSON.stringify(assets, null, 2)});\n\nexport function voiceAssetFor(characterId: CharacterVoiceId, kind: VoiceLineKind) {\n  return VOICE_ASSET_URLS[\`${"${characterId}:${kind}"}\`];\n}\n`;
}

const existing = await readFile(destination, "utf8").catch(() => "");
const existingManifest = existing.match(/Object\.freeze\((\{[\s\S]*?\})\);/);
const assets = existingManifest ? JSON.parse(existingManifest[1]) : {};

for (const line of lines) {
  if (assets[line.key]) {
    console.log(`Keeping ${line.key}.`);
    continue;
  }

  process.stdout.write(`Generating ${line.key}... `);
  let audioUrl = null;
  for (let attempt = 1; attempt <= 3 && !audioUrl; attempt += 1) {
    const response = await fetch(endpoint, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        characterId: line.characterId,
        speakerId: line.speakerId,
        kind: line.kind,
      }),
    });
    const payload = await response.json().catch(() => null);
    if (response.ok && typeof payload?.audioUrl === "string" && payload.audioUrl) {
      audioUrl = payload.audioUrl;
      break;
    }
    if (attempt < 3) await new Promise((resolve) => setTimeout(resolve, 2500));
  }
  if (!audioUrl) throw new Error(`${line.key} failed after 3 attempts.`);
  assets[line.key] = audioUrl;
  await writeFile(destination, renderManifest(assets), "utf8");
  process.stdout.write("done\n");
}

await writeFile(destination, renderManifest(assets), "utf8");
console.log(`Wrote ${Object.keys(assets).length} immutable voice assets.`);
