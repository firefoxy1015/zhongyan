import { readFile, writeFile } from "node:fs/promises";

import { LIAR_GAME } from "../app/lib/liar-game.ts";
import {
  CHARACTER_VOICE_PROFILES,
  FOLLOW_UP_SPEAKER_ID,
} from "../app/lib/testimony-speech.ts";

const apiKey = process.env.LINGKE_API_KEY;
if (!apiKey) throw new Error("LINGKE_API_KEY is required to pre-render voice assets.");

const model = "doubao-tts-2.0";
const endpoint = "https://api.lk888.ai/v1/media/generate";
const statusEndpoint = "https://api.lk888.ai/v1/media/status";
const force = process.env.VOICE_WARM_FORCE === "1";
const lines = LIAR_GAME.stories.flatMap((story) => [
  { key: `${story.id}:testimony`, characterId: story.id, speakerId: story.id, kind: "testimony", line: story.testimony },
  {
    key: `${story.id}:followUp`,
    characterId: story.id,
    speakerId: FOLLOW_UP_SPEAKER_ID,
    kind: "followUp",
    line: story.selfReflection ?? story.followUp,
  },
]);

const destination = new URL("../app/lib/voice-assets.ts", import.meta.url);

function renderManifest(assets, speakers) {
  return `import type { CharacterVoiceId, VoiceLineKind } from "./testimony-speech.ts";\n\nexport type VoiceAssetKey = \`${"${CharacterVoiceId}:${VoiceLineKind}"}\`;\nexport const VOICE_ASSET_MODEL = "doubao-tts-2.0";\n\n// Generated by scripts/warm-voice-assets.mjs. Do not replace these URLs at runtime.\nexport const VOICE_ASSET_URLS: Readonly<Partial<Record<VoiceAssetKey, string>>> = Object.freeze(${JSON.stringify(assets, null, 2)});\n\n// Every follow-up is rendered with Qi Xia's fixed male voice, not the witness voice.\nexport const VOICE_ASSET_SPEAKERS: Readonly<Partial<Record<VoiceAssetKey, CharacterVoiceId>>> = Object.freeze(${JSON.stringify(speakers, null, 2)});\n\nexport function voiceAssetFor(characterId: CharacterVoiceId, kind: VoiceLineKind) {\n  return VOICE_ASSET_URLS[\`${"${characterId}:${kind}"}\`];\n}\n`;
}

const existing = await readFile(destination, "utf8").catch(() => "");
const existingManifest = existing.match(/Object\.freeze\((\{[\s\S]*?\})\);/);
const assets = existingManifest ? JSON.parse(existingManifest[1]) : {};
const speakers = Object.fromEntries(lines.map((line) => [line.key, line.speakerId]));

for (const line of lines) {
  const profile = CHARACTER_VOICE_PROFILES[line.speakerId];
  if (profile.model !== model) {
    throw new Error(`${line.key} is not configured for ${model}.`);
  }
  if (!force && assets[line.key]) {
    console.log(`Keeping ${line.key}.`);
    continue;
  }

  process.stdout.write(`Generating ${line.key}... `);
  let audioUrl = null;
  for (let attempt = 1; attempt <= 3 && !audioUrl; attempt += 1) {
    const response = await fetch(endpoint, {
      method: "POST",
      headers: { "Content-Type": "application/json", Authorization: `Bearer ${apiKey}` },
      body: JSON.stringify({
        model,
        prompt: line.line,
        params: { voice_id: profile.voiceId },
      }),
    });
    const payload = await response.json().catch(() => null);
    const taskId = payload?.data?.task_id ?? payload?.data?.task_ids?.[0];
    if (response.ok && taskId) {
      for (let poll = 0; poll < 24; poll += 1) {
        if (poll > 0) await new Promise((resolve) => setTimeout(resolve, 1500));
        const status = await fetch(`${statusEndpoint}?task_id=${taskId}`, {
          headers: { Authorization: `Bearer ${apiKey}` },
        });
        const result = await status.json().catch(() => null);
        if (result?.state === "success" && typeof result.result_url === "string" && result.result_url) {
          audioUrl = result.result_url;
          break;
        }
        if (result?.is_final === true) break;
      }
    }
    if (attempt < 3) await new Promise((resolve) => setTimeout(resolve, 2500));
  }
  if (!audioUrl) throw new Error(`${line.key} failed after 3 attempts.`);
  assets[line.key] = audioUrl;
  await writeFile(destination, renderManifest(assets, speakers), "utf8");
  process.stdout.write("done\n");
}

await writeFile(destination, renderManifest(assets, speakers), "utf8");
console.log(`Wrote ${Object.keys(assets).length} immutable voice assets.`);
